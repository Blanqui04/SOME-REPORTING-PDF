def get_translations(locale: str) -> dict[str, str]:
    """Return the full translation dictionary for *locale*.

    Falls back to ``DEFAULT_LOCALE`` if the requested locale is unknown.

    Args:
        locale: ISO 639-1 language code (e.g. ``"ca"``, ``"en"``).

    Returns:
        A mapping of translation keys to localised strings.
    """
    return _TRANSLATIONS.get(locale, _TRANSLATIONS[DEFAULT_LOCALE])


def t(key: str, locale: str = DEFAULT_LOCALE) -> str:
    """Translate a single message key.

    Args:
        key: Dot-separated translation key (e.g. ``"pdf.generated_at"``).
        locale: ISO 639-1 language code.

    Returns:
        Translated string, or the key itself if not found.
    """
    translations = get_translations(locale)
    return translations.get(key, key)


def get_locale_name(locale: str) -> str:
    """Return the human-readable name of a locale.

    Args:
        locale: ISO 639-1 language code.

    Returns:
        Locale display name.
    """
    names = {
        "ca": "Català",
        "es": "Español",
        "en": "English",
        "pl": "Polski",
    }
    return names.get(locale, locale)
